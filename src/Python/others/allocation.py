# 変換関数
def convert_allocation(allocation):
    result = []
    for entry in allocation:
        start, end = entry[0]
        value = entry[1]
        result.append(f"{{{{{start[0]}, {start[1]}}}, {{{end[0]}, {end[1]}}}, {value:.1f}}}")
    return result

# 配列を変換
